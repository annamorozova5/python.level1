str = "Beautiful life"
pod_str = str[:12]   #получим подстроку с индексами с 0 по 12 (не включая 12-й)
print(pod_str)

pod_str1 = str[2:10:3]   #получим подстроку с индексами со 2-го по 10-й (не включая) через 2 символа
print(pod_str1)

pod_str2 = str[5:15]   #получим подстроку с индексами с 5-го по 15-й (не включая)
print(pod_str2)
